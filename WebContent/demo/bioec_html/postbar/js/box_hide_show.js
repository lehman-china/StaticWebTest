
function indbar(){
	$("#indbar_main").show();
	$("#mybar_main").hide();
	$("#parbar_main").hide();
	$("#colltheme_main").hide();
	$("#partin_main").hide();
	$("#not_reading").hide();
	};
	
function mybar(){
	$("#indbar_main").hide();
	$("#mybar_main").show();
	$("#parbar_main").hide();
	$("#colltheme_main").hide();
	$("#partin_main").hide();
	$("#not_reading").hide();
	};
	
function parbar(){
	$("#indbar_main").hide();
	$("#mybar_main").hide();
	$("#parbar_main").show();
	$("#colltheme_main").hide();
	$("#partin_main").hide();
	$("#not_reading").hide();
	};
	
function colltheme(){
	$("#indbar_main").hide();
	$("#mybar_main").hide();
	$("#parbar_main").hide();
	$("#colltheme_main").show();
	$("#partin_main").hide();
	$("#not_reading").hide();
	};
	
function partin(){
	$("#indbar_main").hide();
	$("#mybar_main").hide();
	$("#parbar_main").hide();
	$("#colltheme_main").hide();
	$("#partin_main").show();
	$("#not_reading").hide();
	};
	
function not_reading(){
	$("#indbar_main").hide();
	$("#mybar_main").hide();
	$("#parbar_main").hide();
	$("#colltheme_main").hide();
	$("#partin_main").hide();
	$("#not_reading").show();
	};